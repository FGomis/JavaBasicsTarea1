
public class JavaBasicsTarea1App {

	public static void main(String[] args) {
		
		int a = 30;
		int b = 5;
		int r;
		
		r=a+b;
		System.out.println("La suma de "+a+" y "+b+" da "+r);
		
		r=a-b;
		System.out.println("La resta "+a+" menos "+b+" da "+r);
		
		r=a*b;
		System.out.println("La multiplicación de "+a+" y "+b+" da "+r);
		
		r=a/b;
		System.out.println("La división de "+a+" entre "+b+" da "+r);
		
		r=a%b;
		System.out.println("El resto de la división entre "+a+" y "+b+" es "+r);
	}

}
